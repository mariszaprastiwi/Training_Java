package Day2;

public class ArrayEx {
	public static void main(String[] args) {
		
		//deklarasi array
		int[]nilai = new int[5];
		int[]point = {1,3,5,6,7};
		String[]nama= new String[3];
		
		
		//isi array
		nilai[0] = 11;
		point[0] = 7;
		nama[0] = "aduh";
		nama[1] = "rambo";
		
		
		//cetak array
		System.out.println(nilai[0]);
		System.out.println(point[0]);
		System.out.println(nama[0]);
		System.out.println(nama[2]);
		
	}

}
